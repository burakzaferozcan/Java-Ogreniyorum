
public class Main {

	public static void main(String[] args) {
		// MATH SINIFI METODLARI
		// Math.floor(x) : kucuk tamsayıya yuvarlar
		// Math.ceil(x) : bir buyuk tamsayıysa yuvarlama
		// Math.round(x) : yakın olan tamsayıya yuvarlar
		// Math.random() : 1-0 arasında rastgele değer verir
		// Math.cos(x) : cos degerini döner
		// Math.sin(x) : sin degerini döner
		// Math.max(x,y) : buyuk sayıyı verir
		// Math.min(x,y) : kuçuk sayıyı verir
		// Math.pow(3,5) : 3^5 üs alır
		// Math.abs(x) : mutlak deger
		// Math.sqrt(x) : karekök alır
		System.out.println(Math.floor(5.5));
		System.out.println(Math.ceil(5.5));
		System.out.println(Math.round(5.4));
		System.out.println(Math.random());
		System.out.println(Math.cos(30));
		System.out.println(Math.sin(90));
		System.out.println(Math.max(5,6));
		System.out.println(Math.min(9,0));
		System.out.println(Math.pow(2,2));
		System.out.println(Math.abs(-50));
		System.out.println(Math.sqrt(16));

	}

}
